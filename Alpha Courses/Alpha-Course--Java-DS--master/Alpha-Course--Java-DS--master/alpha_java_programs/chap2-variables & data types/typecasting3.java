//program on typecasting 
public class typecasting3 {
    public static void main(String[] args) {
        char ch = 'a';
        char ch2 = 'b';
        char ch3 = 'z';
        int number = ch;
        int number2 = ch2;
        int number3 = ch3;
        System.out.println(number);
        System.out.println(number2);
        System.out.println(number3);
    }
}
/*
 * Explanation-
 * in this the value of "a" is 97 and 98 is the value of "b" as it is the next char
 * like that, the value keeps on increasing till 122 which is value of z
 */