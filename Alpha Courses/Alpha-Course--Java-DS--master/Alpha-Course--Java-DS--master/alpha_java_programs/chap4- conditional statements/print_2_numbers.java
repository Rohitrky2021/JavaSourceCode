//prog to print the largest of 2 numbers using if-else 
public class print_2_numbers {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        if(a>b) {
            System.out.println(a);
        }
        else {
            System.out.println(b);
        }
    }
}
/*
 * Explanation-
 * in this we will check if a>b if yes then it is true and print "a" otherwise if this condition is false print "b"
 */