//WAP on if-else statement to check whether a person is eligible to drive or no
public class if_else_prog1 {
    public static void main(String[] args) {
        int age = 22;
        if(age >=18) 
        {
            System.out.println("you can drive");
        }
        else 
        {
            System.out.println("you cannot drive");
        }
    }
}
/*
 * Explanation-
 * in this prog if the age of the person is more than 18 then we can drive otherwise the condition goes to else statement
 * and shows that you can't drive
 */