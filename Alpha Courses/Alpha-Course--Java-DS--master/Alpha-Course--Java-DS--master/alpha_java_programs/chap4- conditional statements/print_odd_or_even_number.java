//prog to chekc if number is odd or even
public class print_odd_or_even_number {
    public static void main(String[] args) {
        int number = 10;
        if(number %2 == 0) 
        {
           System.out.println("the number is even"); 
        }
        else 
        {
            System.out.println("the number is odd");
        }
    }
}
/*
 * Explanation-
 * if a number is even then it is divisible by 2 or multiple by 2
 * eg: 8/2, 10/2
 * if even number is % of 2 then answer will be 0
 */