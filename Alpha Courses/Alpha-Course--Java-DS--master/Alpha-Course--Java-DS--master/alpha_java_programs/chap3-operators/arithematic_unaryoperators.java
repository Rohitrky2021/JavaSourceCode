//prog on unary operators- pre-increment type
public class arithematic_unaryoperators {
    public static void main(String[] args) {
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);
    }
}
/*
 * Explanation-
 * in this prog, a is assigned the value 10 but b is pre incremented by a so it shows a+1 and hence the value of
 * a increases by 1 which is 11 and 11 is assigned to b
 */