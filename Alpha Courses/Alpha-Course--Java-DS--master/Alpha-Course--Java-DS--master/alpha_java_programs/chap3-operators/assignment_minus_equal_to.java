//prog on assignment- minus equal to
public class assignment_minus_equal_to {
    public static void main(String[] args) {
        int a = 10;
        a-=10;
        System.out.println(a);
    }
}
/*
 * Explanation-
 * in this prog the a-=10 will show that a=a-10 which will give the output as 5 as the value is 10
 */