//prog on assignment- multiply equal to
public class assignment_multiply_equal_to {
    public static void main(String[] args) {
        int a = 10;
        a*=10;
        System.out.println(a);
    }
}
/*
 * Explanation-
 * in this prog a*=10 will show that a=a*10 which will multiply 10*10 as the value of a is 10
 */