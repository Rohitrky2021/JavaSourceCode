//prog on assignment- modulus equal to
public class assignment_modulus_equal_to {
    public static void main(String[] args) {
        int a = 10;
        a%=10;
        System.out.println(a);
    }
}
/*
 * Explanation-
 * in this prog a%=10 it will show that a=a%10 which gives the remainder as 10 
 */