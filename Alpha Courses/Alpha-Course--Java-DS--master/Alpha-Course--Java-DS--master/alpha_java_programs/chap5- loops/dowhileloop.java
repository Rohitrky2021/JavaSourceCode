//WAP to print hello world 10 times using do while loop
public class dowhileloop {
    public static void main(String[] args) {
        int counter = 1;
        do {
            System.out.println("hello world");
            counter++;
        } while(counter<=10);
    }
}
/*
 * Explanation-
 * in this the counter starts from 1 to 10 so it prints hello world as the output 10 times and gives the output
 */