//WAP to print numbers from 1 to 10 using while loop in java
public class printnumberfrom1to10 {
    public static void main(String[] args) {
        int counter = 1;
        while(counter <=10) {
            System.out.print(counter+" ");
            counter++;
        }
        System.out.println();
    }
}
/*
 * Explanation-
 * in this we print the numbers from 1 to 10 till the counter reaches 10 & then stops the prog and prints the output
 */