def is_sorted(arr):
    n = len(arr)
    for i in range(n - 1):
        if arr[i] > arr[i + 1]:
            return False
    return True

# Example usage:
arr = [1, 2, 3, 5, 4]
result = is_sorted(arr)
print(result)



